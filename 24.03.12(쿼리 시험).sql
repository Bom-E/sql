	EMPNO
	, ENAME
	, HIREDATE
FROM emp
WHERE ENAME LIKE ('이__')
ORDER BY EMPNO;

SELECT
	EMPNO
	, ENAME
	, E.DEPTNO
	, DNAME
	, CASE
		WHEN D.DEPTNO = 10 THEN '인사부'
		WHEN D.DEPTNO = 20 THEN '생산부'
		END AS '바뀐거'
FROM emp AS E
INNER JOIN dept AS D
ON E.DEPTNO = D.DEPTNO;

